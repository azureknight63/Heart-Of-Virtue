#!/usr/bin/env node
/**
 * Generate frontend/src/utils/sfxDurations.js from the SHIPPED WAV files.
 *
 * The duration source of truth is the actual bytes in
 * public/assets/sounds/sfx_*.wav — correct no matter how a file was produced
 * (synthesized, hand-picked among renders, or trimmed in Audacity). The 75%
 * partial-stack SFX scheduler (issue #436) reads this manifest to time
 * emissions. Re-run whenever the audio changes.
 *
 *   node scripts/generate-sfx-durations.mjs          # (re)write the manifest
 *   node scripts/generate-sfx-durations.mjs --check   # fail if stale (CI/prebuild)
 *
 * See docs/development/combat-streaming-plan.md.
 */
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SOUNDS_DIR = join(__dirname, '..', 'public', 'assets', 'sounds');
const OUT_FILE = join(__dirname, '..', 'src', 'utils', 'sfxDurations.js');

/**
 * Exact duration (ms) of a PCM WAV from its header: data-chunk bytes / byteRate.
 * Walks the RIFF chunk list (chunks aren't always adjacent). Returns null for a
 * non-PCM / unparseable file so the caller can warn rather than emit a wrong number.
 */
export function wavDurationMs(buffer) {
  if (buffer.length < 12 || buffer.toString('ascii', 0, 4) !== 'RIFF') return null;
  if (buffer.toString('ascii', 8, 12) !== 'WAVE') return null;

  let byteRate = null;
  let dataSize = null;
  let offset = 12;
  while (offset + 8 <= buffer.length) {
    const id = buffer.toString('ascii', offset, offset + 4);
    const size = buffer.readUInt32LE(offset + 4);
    const body = offset + 8;
    if (id === 'fmt ' && body + 16 <= buffer.length) {
      byteRate = buffer.readUInt32LE(body + 8);
    } else if (id === 'data') {
      dataSize = size;
    }
    // Chunks are word-aligned: a padding byte follows an odd size.
    offset = body + size + (size % 2);
  }
  if (!byteRate || dataSize == null) return null;
  return Math.round((dataSize / byteRate) * 1000);
}

const cueOf = (filename) => filename.replace(/^sfx_/, '').replace(/\.wav$/, '');

/** Map cue -> duration_ms for every sfx_*.wav in the sounds dir. */
export function computeDurations(soundsDir = SOUNDS_DIR) {
  const durations = {};
  const warnings = [];
  for (const name of readdirSync(soundsDir)) {
    if (!name.startsWith('sfx_') || !name.endsWith('.wav')) continue;
    const ms = wavDurationMs(readFileSync(join(soundsDir, name)));
    if (ms == null) {
      warnings.push(name);
      continue;
    }
    durations[cueOf(name)] = ms;
  }
  return { durations, warnings };
}

function renderManifest(durations) {
  const entries = Object.keys(durations)
    .sort()
    .map((cue) => `  ${JSON.stringify(cue)}: ${durations[cue]},`)
    .join('\n');
  return (
    '// AUTO-GENERATED by scripts/generate-sfx-durations.mjs — do not edit by hand.\n' +
    '// Natural (1x) playback length in ms of each shipped sfx_<cue>.wav, read from\n' +
    '// the WAV header. Regenerate after changing audio: `npm run sfx:durations`.\n' +
    '// See docs/development/combat-streaming-plan.md (issue #436).\n\n' +
    'export const SFX_DURATIONS = {\n' +
    entries +
    '\n};\n'
  );
}

function main() {
  const check = process.argv.includes('--check');
  const { durations, warnings } = computeDurations();
  for (const name of warnings) {
    console.warn(`[sfx-durations] skipped non-PCM/unparseable WAV: ${name}`);
  }
  const next = renderManifest(durations);

  if (check) {
    let current = '';
    try {
      current = readFileSync(OUT_FILE, 'utf8');
    } catch {
      /* missing file -> stale */
    }
    if (current !== next) {
      console.error(
        '[sfx-durations] sfxDurations.js is stale. Run: npm run sfx:durations'
      );
      process.exit(1);
    }
    console.log('[sfx-durations] manifest is up to date.');
    return;
  }

  writeFileSync(OUT_FILE, next);
  console.log(
    `[sfx-durations] wrote ${Object.keys(durations).length} cues to ${OUT_FILE}`
  );
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}
