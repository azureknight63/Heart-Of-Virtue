#!/usr/bin/env python
"""Acceptance test generator for Heart of Virtue features.

Generates minimal test infrastructure (maps, scenarios, config files, scripts)
for rapid feature validation. Integrates with the bug-hunt harness and browser
testing (gstack /qa).

Usage:
    python tools/acceptance_test_generator.py \
        --feature "cooldown drain on passive moves" \
        --output tests/acceptance/cooldown-passive
"""

import argparse
import json
import textwrap
from pathlib import Path
import sys

ROOT = Path(__file__).resolve().parent.parent
SRC_DIR = ROOT / "src"


class TestArtifact:
    """Represents a single generated test artifact (file to write)."""

    def __init__(self, path: Path, content: str, description: str = ""):
        self.path = path
        self.content = content
        self.description = description

    def write(self) -> None:
        """Write the artifact to disk."""
        self.path.parent.mkdir(parents=True, exist_ok=True)
        self.path.write_text(self.content)
        print(f"  ✓ {self.path.relative_to(ROOT)} — {self.description}")


