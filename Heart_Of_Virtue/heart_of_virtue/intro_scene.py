import time
import functions

def intro():
    functions.print_slow("Darkness. Silence. You are surrounded by these. They seem to hang around you like a thick fog,"
               " suffocating your senses, your consciousness. You try to cry out into the void, but you can't feel"
               " your mouth moving. You can't feel anything. You'd panic but that all seems so pointless now, here"
               " in this thick soup of nullity. Not even the sound of your own heart beating or your breath"
               " escaping your lungs seems to penetrate the blackness and the quiet.\n")
    time.sleep(10)
    functions.screen_clear()
    functions.print_slow("Gradually, a noise begins to rise out of the void. Indistinct and quiet at first, like a whisper, "
               "but slowly coming into focus.\n\n\n")
    time.sleep(5)
    functions.screen_clear()
    functions.print_slow("'The body of Christ...'\n\n")
    time.sleep(5)
    functions.screen_clear()
    functions.print_slow("'The body of Christ...'\n\n")
    time.sleep(5)
    functions.screen_clear()
    functions.print_slow("Slowly, light and sound begin to pour back into your awareness. After what seems like an "
               "eternity, you open your eyes...\n\n")
    time.sleep(10)
    functions.screen_clear()
    return
