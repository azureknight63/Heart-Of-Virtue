        recoil = 1  # modified later, based on player weapon
        cooldown = 5 - int(player.speed / 10)
        if cooldown < 0:
            cooldown = 0
        weapon = "fist"  # modified later, based on player weapon
        fatigue_cost = 100 - (5 * player.endurance)
        if fatigue_cost <= 10:
            fatigue_cost = 10
        mvrange = (0, 5)
